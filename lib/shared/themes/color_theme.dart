import 'package:flutter/material.dart';

class CustomColorTheme {
  static ColorScheme theme() {
    return ColorScheme.fromSeed(
      seedColor: const Color(0xFFD9B88F),
      primary: const Color(0xFFD9B88F),
      secondary: const Color(0xFF594020),
      tertiary: const Color(0xFF594C3B),
      background: const Color(0xFFFEFDF9),
    );
  }
}
